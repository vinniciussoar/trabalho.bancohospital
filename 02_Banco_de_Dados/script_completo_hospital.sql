    ITEM_ID NUMBER GENERATED ALWAYS AS IDENTITY PRIMARY KEY,
    REGISTRO_ID NUMBER NOT NULL,
    SERVICO_ID NUMBER NOT NULL,
    QUANTIDADE NUMBER DEFAULT 1,
