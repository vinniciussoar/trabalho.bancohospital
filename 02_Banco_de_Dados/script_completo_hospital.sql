    FOREIGN KEY (REGISTRO_ID) REFERENCES REGISTRO_ATENDIMENTO(REGISTRO_ID),
    FOREIGN KEY (SERVICO_ID) REFERENCES CATALOGO_SERVICO(SERVICO_ID)
);

